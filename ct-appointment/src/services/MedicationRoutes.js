export async function addNewMedication(name, dose, packageSize) {

  return api.post('/api/medication', {
    name: name,
    dose: dose,
    packageSize: packageSize
  }).then(res => {
    console.log(res.status);
    return res.data
  })
    .catch(err => {
      console.log('error', err)
      throw err;
    })
}

